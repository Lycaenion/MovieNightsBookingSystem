class Event{

    get movieTitle(){
        return this._movieTitle;
    }

    get date(){
        return this._date;
    }

    get attending(){
        return this._attending;
    }

    set movieTitle(movieTitle){
        this._movieTitle = movieTitle;
    }

    set date(date){
        this._date = date;
    }

    set attending(attending){
        this._attending = attending;
    }


}